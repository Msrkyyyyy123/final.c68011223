#include <stdio.h>

int main() {
    // ประกาศตัวแปร
    int a = 15, b = 10;
    
    // ตัวดำเนินการคณิตศาสตร์
    int sum = a + b;       // ผลบวก
    int diff = a - b;      // ผลต่าง
    int mult = a * b;   // ผลคูณ
    int div = a / b;  // ผลหาร (ใช้ float จะเหมาะสมกว่า)
    int remain = a % b; // ผลหารเศษ
    
    // แสดงผลลัพธ์
    printf("Sum: %d\n", sum);
    printf("Difference: %d\n", diff);
    printf("Product: %d\n", mult);
    printf("Quotient: %d\n", div);
    printf("Remainder: %d\n", remain);
    
    printf("hello\n");
    printf("hellooo\n");
    printf("hellooooooo\n");
    printf("hellooooooooooo\n");
    printf("hellooooooooooooooooo\n");

    return 0;
}
